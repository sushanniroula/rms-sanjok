            <h1><mark>Introduction</mark></h1>

            <p><b>The arts</b> refers to the theory and physical expression of creativity found in human societies and cultures. Major constituents of the arts include literature (including drama, poetry, and prose), performing arts (among them dance, music, and theatre), and visual arts (including drawing, painting, filmmaking, architecture, ceramics, sculpting, and photography).</p>

            <p>Some art forms combine a visual element with performance (e.g., cinematography) or artwork with the written word (e.g., comics). From prehistoric cave paintings to modern-day films, art serves as a vessel for storytelling and conveying humankind's relationship with the environment.</p>

            <h1><mark>Definitions</mark></h1>
            <p>In its most basic abstract definition, art is a documented expression of a sentient being through or on an accessible medium so that anyone can view, hear, or experience it. The act itself of producing an expression can also be referred to as a certain art or as art in general. If this solidified expression, or the act of producing it, is "good" or has value depends on those who access and rate it, and this public rating is dependent on various subjective factors. Merriam-Webster defines "the arts" as "painting, sculpture, music, theatre, literature, etc., considered as a group of activities done by people with skill and imagination." Similarly, the United States Congress, in the National Foundation on the Arts and Humanities Act, defined "the arts" as follows:</p>

            <p>The term 'the arts' includes, but is not limited to, music (instrumental and vocal), dance, drama, folk art, creative writing, architecture and allied fields, painting, sculpture, photography, graphic and craft arts, industrial design, costume and fashion design, motion pictures, television, radio, film, video, tape and sound recording, the arts related to the presentation, performance, execution, and exhibition of such major art forms, all those traditional arts practiced by the diverse peoples of this country. (sic) and the study and application of the arts to the human environment.</p>

            <h1><mark>History</mark></h1>
            <p>Main article: History of art
            In Ancient Greece, all art and craft was referred to by the same word, techne. Thus, there was no distinction among the arts. Ancient Greek art brought the veneration of the animal form and the development of equivalent skills to show musculature, poise, beauty, and anatomically correct proportions. Ancient Roman art depicted gods as idealized humans, shown with characteristic distinguishing features (e.g. Zeus' thunderbolt). In Byzantine and Gothic art of the Middle Ages, the dominance of the church insisted on the expression of biblical and not material truths. Eastern art has generally worked in a style akin to Western medieval art, namely a concentration on surface patterning and local colour (meaning the plain color of an object, such as basic red for a red robe, rather than the modulations of that color brought about by light, shade, and reflection). A characteristic of this style is that the local color is often defined by an outline (a contemporary equivalent is the cartoon). This is evident in, for example, the art of India, Tibet, and Japan. Religious Islamic art forbids iconography and expresses religious ideas through geometry instead.</p>

            <h1><mark>Classifications</mark></h1>

            <p>Lawrence Alma-Tadema's Catullus-at-Lesbia's (1865)
            In the Middle Ages, the Artes Liberales (liberal arts) were taught in universities as part of the Trivium, an introductory curriculum involving grammar, rhetoric, and logic, and of the Quadrivium, a curriculum involving the "mathematical arts" of arithmetic, geometry, music, and astronomy. The Artes Mechanicae (consisting of vestiaria – tailoring and weaving; agricultura – agriculture; architectura – architecture and masonry; militia and venatoria – warfare, hunting, military education, and the martial arts; mercatura – trade; coquinaria – cooking; and metallaria – blacksmithing and metallurgy) were practiced and developed in guild environments. The modern distinction between "artistic" and "non-artistic" skills did not develop until the Renaissance. In modern academia, the arts are usually grouped with or as a subset of the humanities. Some subjects in the humanities are history, linguistics, literature, theology, philosophy, and logic.</p>

            <p>The arts have also been classified as seven: painting, architecture, sculpture, literature, music, performing, and cinema. Some view literature, painting, sculpture, and music as the main four arts, of which the others are derivative; drama is literature with acting, dance is music expressed through motion, and song is music with literature and voice.</p>
