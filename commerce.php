<b><h1><mark>Introduction</mark></h1></b>

<p><b><u>Commerce</u></b> relates to <b>"the exchange of goods and services, especially on a large scale."</b>It includes legal, economic, political, social, cultural and technological systems that operate in a country or in international trade.</p>

<b><h1><mark>Etymology</b></h1></mark>
<p>The English-language word commerce has been derived from the Latin word <B>commercium</B>, from <b>cum ("together")</b>and <b>merx ("merchandise")</b></p>.

<b><h1><mark>History</b></h1></mark>

<p>The <b>caduceus</b> - used today as the symbol of commerce,and traditionally associated with the Roman god Mercury, patron of commerce, trickery and thieves.
Some commentators[which?] trace the origins of commerce to the very start of transactions in prehistoric times. Apart from traditional self-sufficiency, trading became a principal facility of prehistoric people, who bartered what they had for goods and services from each other (the barter system was popular in ancient times where one could get goods and services by offering the other person some other good and service according to their need instead of paying with monetary systems, which developed later). Historian Peter Watson and Ramesh Manickam date the history of long-distance commerce from circa 150,000 years ago.</p>

<p>In historic times, the introduction of currency as a standardized money facilitated the wider exchange of goods and services. Numismatists have collections of tokens, which include coins from some Ancient-World large-scale societies, although initial usage involved unmarked lumps of precious metal.</p>

<p>The circulation of a standardized currency provides a method of overcoming the major disadvantage to commerce through use of a barter system, the <b>"double coincidence of wants" </b>(which means if someone wants something from a person, that person should also be in need of a thing or a service which they can provide), necessary for barter trades to occur. For example, if a person who makes pots for a living needs a new house, he/she may wish to hire someone to build it for him/her. But he/she cannot make an equivalent number of pots to equal this service done for him/her, because even if the builder could build the house, the builder might not want many or any pots. Also, the barter system had a major drawback in that whatever goods a person get as payment may not necessarily store for long amounts of time. For example: if a person has got dozens of fruits as his payment, he/she can't store fruit for long or they may rot - which means a person will have to bear a huge loss. Currency solved this problem by allowing a society as a whole to assign values[citation needed] and thus to collect goods and services effectively and to store them for later use, or to split them among minions.</p>

<p>During the Middle Ages, commerce developed in Europe through the trading of luxury goods at trade fairs. Some wealth became converted into movable wealth or capital.[citation needed] Banking systems developed where money on account was transferred[by whom?] across national boundaries.Hand-to-hand markets became a feature of town life, and were regulated by town authorities.</p>

<p>Today commerce includes as a subset of itself a complex system of companies which try to maximize their profits by offering products and services to the market (which consists both of individuals and groups and other companies or institutions) at the lowest production cost. A system of international trade has helped to develop the world economy; but, in combination with bilateral or multilateral agreements to lower tariffs or to achieve free trade, has sometimes harmed third-world markets for local products (see Globalization.)</p>
